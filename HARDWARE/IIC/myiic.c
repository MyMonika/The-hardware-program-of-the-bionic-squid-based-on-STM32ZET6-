u8 i2c_CheckDevice(uint8_t _Address)
{
	uint8_t ucAck;

	IIC_Init();		/* 配置GPIO */

	IIC_Start();		/* 发送启动信号 */
  
	/* 发送设备地址+读写控制bit（0 = w， 1 = r) bit7 先传 */
        ///////////////////////居然不移位、、、、、、、、、、、、
	IIC_Send_Byte(_Address<<1 | 0x00);
	ucAck = IIC_Wait_Ack();	/* 检测设备的ACK应答 */
	IIC_Stop();			/* 发送停止信号 */
        if(!ucAck){
          printf("同地址为%d的PCA9685通讯成功\r\n",_Address);
					return 1;
				}
				else
				{
					printf("通讯失败");
					return 0;
				}
}









































